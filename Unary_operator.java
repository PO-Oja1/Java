public class Unary_operator {
    public static void main(String arg[]){
        int num = 12;
        int a = -num;
        int b = +-num;

        int c = -(-num);

        System.out.println(num);
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println();
    }
}
